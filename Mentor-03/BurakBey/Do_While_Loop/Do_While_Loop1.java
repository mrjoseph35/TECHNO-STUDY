package Mentor.BurakBey.Do_While_Loop;

public class Do_While_Loop1 {
    public static void main(String[] args) {
        // TODO ----- While ile Do-While in Farki
        /* Bu kodda sarta uygun olmadigi durumda
            While hic bir cikti vermezken
            Do-While Bir kere cikti veriyor. Sart sonda kontrol edildigi icin
        */

        // While

        int i = 10;

        while (i<5){

            System.out.println("Hello While");
        }

        //do-while

        int a = 10;

        do {
            System.out.println("Hello do-while");

        }while (a< 5);
    }
}
